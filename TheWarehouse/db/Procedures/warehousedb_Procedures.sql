	@idTransactions int,
	@EmployeeId int,
	@Total decimal(6, 2),
	@Code varchar(135),
	@timestamp datetime
AS

SET NOCOUNT ON

IF EXISTS(SELECT [idTransactions] FROM [transactions] WHERE [idTransactions] = @idTransactions)
BEGIN
	UPDATE [transactions] SET
		[EmployeeId] = @EmployeeId,
		[Total] = @Total,
		[Code] = @Code,
		[timestamp] = @timestamp
	WHERE
		[idTransactions] = @idTransactions
END
ELSE
BEGIN
	INSERT INTO [transactions] (
		[idTransactions],
		[EmployeeId],
		[Total],
		[Code],
		[timestamp]
	) VALUES (
		@idTransactions,
		@EmployeeId,
		@Total,
		@Code,
		@timestamp
	)
END

--endregion

GO

